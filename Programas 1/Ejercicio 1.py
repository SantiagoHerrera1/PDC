#Cálcular el perímetro y el área de un cuadrado dado el valor de uno de sus lados l.

#Input:lado
#Output:perímetro y área

lado=float(input("Digite el valor de uno de los lados de su cuadrado"))
Perímetro=lado*4
Área=lado**2

print("El perímetro de su cuadrado es:", Perímetro)
print("El área de su cuadrado es:", Área)